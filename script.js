var canvas = document.getElementById("CanvasCanvas"); // 获取画布元素
var ctx = canvas.getContext("2d"); // 获取2D渲染上下文
var img = document.createElement("img"); // 创建图片元素


function canvasDraw() {
    canvas.getContext("2d").clearRect(0, 0, canvas.width, canvas.height); // 清空画布
    var keys = Object.keys(zun23); // 获取所有键名
    var imageQueue = []; // 图片队列
    for (var i = keys.length - 1; i >= 0; i--) { // 倒序遍历所有键名
        var key = keys[i]; // 获取当前键名
        var data = zun23[key]; // 获取当前样式数组
        for (var j = data.length - 1; j >= 0; j--) { // 倒序遍历所有样式对象
            var style = data[j]; //当前样式
            if (style.enabled) { // 如果启用
                imageQueue.push(style); // 添加到队列
            }
        }
    }

    if (imageQueue.length === 0) { // 如果队列为空
        document.getElementById("CanvasImg").src = canvas.toDataURL(); // 设置图片源
        return;
    }
    
    // 单线程逐个加载和绘制图片
    var currentIndex = 0; // 当前索引
    
    function loadNextImage() { // 加载下一张图片
        if (currentIndex >= imageQueue.length) { // 如果索引超出范围
            // 所有图片绘制完成后更新预览
            try {
                document.getElementById("CanvasImg").src = canvas.toDataURL();
            } catch (e) {
                console.error("Error exporting canvas:", e);
                // 可以在这里添加备用方案
            }
            return;
        }
        
        var style = imageQueue[currentIndex]; // 获取当前样式
        // 移除 crossOrigin 属性，因为在本地文件系统中会导致 CORS 错误
        img.onload = function() { // 图片加载完成
            if (style.visible || style.visible == undefined) { // 如果可见
                ctx.drawImage(img, style.x, style.y, img.width*0.4, img.height*0.4); // 绘制图片到画布
            }
            currentIndex++;
            loadNextImage();
        };
        img.onerror = function() {
            // 图片加载失败，继续加载下一张
            currentIndex++;
            loadNextImage();
        };
        
        img.src = style.path; // 设置图片源
    }
    
    // 开始加载第一张图片
    loadNextImage();
}
