function DecideBox(source,data,canMultiSelect = true) { // 添加头顶样式选项
    var select = document.getElementById(source); // 获取选择框
    for (var i = 0; i < data.length; i++) { // 遍历头顶样式对象
        (function(style) { // 匿名函数
        var item = document.createElement("div"); // 创建选项元素
        item.id = style.name; // 设置ID
        item.classList.add("item"); // 添加类名
        var img = document.createElement("img"); // 创建图片元素
        img.src = style.path; // 设置图片源
        item.appendChild(img); // 添加图片到选项
        var p = document.createElement("p"); // 创建文本元素
        p.textContent = style.name; // 设置文本内容为名称
        item.appendChild(p); // 添加文本到选项
        item.addEventListener("click", function() { // 点击事件
        if (!canMultiSelect) { // 如果不能多选
            for (var j = 0; j < data.length; j++) { // 遍历所有样式对象
                if (data[j].enabled){ // 如果启用
                    if (data[j].name != style.name){ // 如果不是当前样式
                        data[j].enabled = false; // 禁用其他样式
                    }
                }
            }
        }
        style.enabled = !style.enabled; // 切换启用状态
        showText("["+style.name+(style.enabled?"已启用":"已禁用")+"]",'white',true,1000);
        updateHeadStyle(data,style.name); // 更新头顶样式
        }); // 添加点击事件
        select.appendChild(item); // 添加选项到选择框
        })(data[i]); // 获取当前样式
    }
}
